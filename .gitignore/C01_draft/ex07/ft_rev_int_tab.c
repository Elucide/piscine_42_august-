void	ft_swap(int *a, int *b)
{
	int	c;

	c = *a;
	*a = *b;
	*b = c;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int	i;

	i = size / 2;
	while (i != -1)
	{
		ft_swap(tab[i], tab[size - i - 1]);
		i--;
	}
}
