int	ft_atoi(char *str)
{
	int	i;
	int	signe;
	int	out;

	out = 0;
	i = 0;
	signe = 1;
	while (str[i] <= 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		out = out * 10 + (str[i] - '0');
		i++;
	}
	return (out * signe);
}

#include <stdio.h>
int	main(int ac, char **av)
{
	printf("%d", ft_atoi(av[ac - 1]));
}
